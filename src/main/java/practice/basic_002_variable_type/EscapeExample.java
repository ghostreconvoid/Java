package practice.basic_002_variable_type;

public class EscapeExample {
    public static void main(String[] args) {
        System.out.println("number\tname\tjob");
        System.out.println("new line\n");   // new line x2 : one from \n and another one from println
        System.out.println("new line\n");   // new line x2 : one from \n and another one from println
        System.out.println("We are \"developers\".");
        System.out.println("Spring\\Summer\\Fall\\Winter");
    }
}
